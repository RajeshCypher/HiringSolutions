    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String name=jTextField1.getText().toString();
        String email=jTextField2.getText().toString();
        String password=jTextField3.getText().toString();
        String college=jTextField4.getText().toString();
        String job=jTextField5.getText().toString();
        String year=jTextField6.getText().toString();
        
        if(!name.equals("")&&!email.equals("")&&!password.equals("")&&!college.equals("")&&!job.equals("")&&!year.equals("")){
            int atpos=email.indexOf("@");
            int dotpos=email.lastIndexOf(".");
            if(atpos<1||dotpos<atpos+2||dotpos+2>=email.length()){
                JOptionPane.showMessageDialog(null,"Invalid email address");
            }
            else{
                Connection conn=null;
                PreparedStatement pst=null;

                try{
                    Class.forName("com.mysql.jdbc.Driver");
                    conn=DriverManager.getConnection("jdbc:mysql://localhost:3306/hiringsolutions","root","");

                    pst=conn.prepareStatement("insert into students (name,email,password,college,job,year) values ('"+name+"','"+email+"','"+password+"','"+college+"','"+job+"','"+year+"')");

                    int i=pst.executeUpdate();

                    if(i>0){
                        jTextField1.setText("");
                        jTextField2.setText("");
                        jTextField3.setText("");
                        jTextField4.setText("");
                        jTextField5.setText("");
                        jTextField6.setText("");
                        JOptionPane.showMessageDialog(null,"New Accout Created!\nYou can login now.");
                    }
                    else{
                        JOptionPane.showMessageDialog(null,"Couldn't create Account!\nTry again later.");
                    }

                } catch (SQLException ex) {
                    Logger.getLogger(StudentRegister.class.getName()).log(Level.SEVERE, null, ex);
                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(StudentRegister.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
        else{
            JOptionPane.showMessageDialog(null,"Please fill all details");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

